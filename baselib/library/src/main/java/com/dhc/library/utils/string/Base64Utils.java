    /**
     * 加密
     *
     * @param data
     * @return
     */
    public static String encode(byte[] data)
    {
        StringBuffer sb = new StringBuffer();
        int len = data.length;
        int i = 0;
        int b1, b2, b3;
        while (i < len)
        {
            b1 = data[i++] & 0xff;
            if (i == len)
            {
                sb.append(base64EncodeChars[b1 >>> 2]);
                sb.append(base64EncodeChars[(b1 & 0x3) << 4]);
                sb.append("==");
                break;
            }
            b2 = data[i++] & 0xff;
            if (i == len)
            {
                sb.append(base64EncodeChars[b1 >>> 2]);
                sb.append(base64EncodeChars[((b1 & 0x03) << 4) | ((b2 & 0xf0) >>> 4)]);
                sb.append(base64EncodeChars[(b2 & 0x0f) << 2]);
                sb.append("=");
                break;
            }
            b3 = data[i++] & 0xff;
            sb.append(base64EncodeChars[b1 >>> 2]);
            sb.append(base64EncodeChars[((b1 & 0x03) << 4) | ((b2 & 0xf0) >>> 4)]);
            sb.append(base64EncodeChars[((b2 & 0x0f) << 2) | ((b3 & 0xc0) >>> 6)]);
            sb.append(base64EncodeChars[b3 & 0x3f]);
        }
        return sb.toString();
    }

    /**
     * 解密
     *
     * @param str
     * @return
     */
    public static byte[] decode(String str)
    {
        try
        {
            return decodePrivate(str);
        } catch (UnsupportedEncodingException e)
        {
            e.printStackTrace();
        }
        return new byte[]
                {};
    }

    private static byte[] decodePrivate(String str) throws UnsupportedEncodingException
    {
        StringBuffer sb = new StringBuffer();
        byte[] data = null;
        data = str.getBytes("US-ASCII");
        int len = data.length;
        int i = 0;
        int b1, b2, b3, b4;
        while (i < len)
        {

            do
            {
                b1 = base64DecodeChars[data[i++]];
            } while (i < len && b1 == -1);
            if (b1 == -1)
                break;

            do
            {
                b2 = base64DecodeChars[data[i++]];
            } while (i < len && b2 == -1);
            if (b2 == -1)
                break;
            sb.append((char) ((b1 << 2) | ((b2 & 0x30) >>> 4)));

            do
            {
                b3 = data[i++];
                if (b3 == 61)
                    return sb.toString().getBytes("iso8859-1");
                b3 = base64DecodeChars[b3];
            } while (i < len && b3 == -1);
            if (b3 == -1)
                break;
            sb.append((char) (((b2 & 0x0f) << 4) | ((b3 & 0x3c) >>> 2)));

            do
            {
                b4 = data[i++];
                if (b4 == 61)
                    return sb.toString().getBytes("iso8859-1");
                b4 = base64DecodeChars[b4];
            } while (i < len && b4 == -1);
            if (b4 == -1)
                break;
            sb.append((char) (((b3 & 0x03) << 6) | b4));
        }
        return sb.toString().getBytes("iso8859-1");
    }


    /**
     * 讲bitmap转为base64字符串
     *
     * @param clientBitmap 图片
     * @return
     */
    public static String encodeBase64Bitmap(Bitmap clientBitmap) {

    	/* Bitmap clientBitmap = BitmapFactory.decodeResource(getResources(), R.drawable.messi);*/
        //2.bitmap->byte[]
        ByteArrayOutputStream streem = new ByteArrayOutputStream();
        clientBitmap.compress(Bitmap.CompressFormat.JPEG, 100, streem);
        byte[] clientBytes = streem.toByteArray();
        //3.byte[] - > String
        String clientResult = Base64.encodeToString(clientBytes, Base64.DEFAULT);

        return clientResult;
    }

    /**
     * 讲bitmap转为base64字符串
     *
     * @param clientBitmap 图片
     *                     op  为压缩比例
     * @return
     */
    public static String encodeBase64Bitmap(Bitmap clientBitmap, int op) {

    	/* Bitmap clientBitmap = BitmapFactory.decodeResource(getResources(), R.drawable.messi);*/
        //2.bitmap->byte[]
        ByteArrayOutputStream streem = new ByteArrayOutputStream();
        clientBitmap.compress(Bitmap.CompressFormat.JPEG, op, streem);
        byte[] clientBytes = streem.toByteArray();
        //3.byte[] - > String
        String clientResult = Base64.encodeToString(clientBytes, Base64.DEFAULT);

        return clientResult;
    }

    /**
     * 转换为base64字符串
     *
     * @param path 图片路径
     * @return base64 string
     * @throws Exception
     */
    public static String encodeBase64File(String path) throws Exception {
        File file = new File(path);
        FileInputStream inputFile = new FileInputStream(file);
        byte[] buffer = new byte[(int) file.length()];
        inputFile.read(buffer);
        inputFile.close();
        return Base64.encodeToString(buffer, Base64.DEFAULT);
    }


    public static void Base64ToBitmap(String url, ResourceSubscriber<Bitmap> resourceSubscriber) {
        Flowable.just(url).map(s -> {
            byte[] decode = Base64.decode(s.split(",")[1], Base64.DEFAULT);
            Bitmap bitmap = BitmapFactory.decodeByteArray(decode, 0, decode.length);
            return bitmap;
        }).subscribeOn(Schedulers.io())
                .observeOn(AndroidSchedulers.mainThread())
                .subscribe(resourceSubscriber);
    }


